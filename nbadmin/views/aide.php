<div class="aide">

<div class="aide-section">
    <h2>Tableau de bord</h2>
    <p>Le tableau de bord est la page d'accueil de l'administration. Il affiche :</p>
    <ul>
        <li>Les <strong>statistiques</strong> du site (nombre de projets, publiés, photos, pages)</li>
        <li>Le <strong>sélecteur d'image d'accueil</strong> — choisissez le projet dont la photo de couverture sera affichée sur la page d'accueil du site. L'option « Auto » affiche automatiquement le premier projet publié.</li>
    </ul>
</div>

<div class="aide-section">
    <h2>Gestion des projets</h2>

    <h3>Créer un projet</h3>
    <ol>
        <li>Cliquez sur <strong>Projets</strong> dans la navigation</li>
        <li>Cliquez sur <strong>Nouveau projet</strong></li>
        <li>Renseignez le titre, la catégorie, la description et les crédits photographiques</li>
        <li>Le <strong>slug</strong> (adresse URL) est généré automatiquement depuis le titre. Vous pouvez le modifier manuellement.</li>
        <li>Choisissez le statut : <strong>Publié</strong> (visible sur le site) ou <strong>Brouillon</strong> (masqué)</li>
        <li>Cliquez sur <strong>Enregistrer</strong></li>
    </ol>

    <h3>Modifier un projet</h3>
    <p>Depuis la liste des projets, cliquez sur <strong>Modifier</strong> à droite du projet concerné.</p>

    <h3>Supprimer un projet</h3>
    <p>Depuis le formulaire de modification d'un projet, cliquez sur <strong>Supprimer</strong>. Une confirmation vous sera demandée. Cette action est irréversible et supprime également toutes les photos associées.</p>
</div>

<div class="aide-section">
    <h2>Gestion des photos</h2>

    <h3>Ajouter des photos</h3>
    <ol>
        <li>Depuis la liste des projets, cliquez sur <strong>Photos</strong></li>
        <li>Utilisez le bouton <strong>Ajouter des photos</strong> pour sélectionner une ou plusieurs images</li>
        <li>Formats acceptés : JPG, JPEG, PNG, GIF, WEBP</li>
        <li>Taille maximale : 10 Mo par image</li>
    </ol>

    <h3>Photo de couverture</h3>
    <p>La <strong>première photo</strong> de la liste (marquée « COUVERTURE » en vert) est celle qui apparaît dans la navigation et sur la page d'accueil. Pour changer la couverture :</p>
    <ul>
        <li>Cliquez sur <strong>Couverture</strong> sous la photo souhaitée, ou</li>
        <li><strong>Glissez-déposez</strong> la photo en première position</li>
    </ul>

    <h3>Réordonner les photos</h3>
    <p>Faites <strong>glisser-déposer</strong> les photos pour modifier leur ordre d'affichage sur le site.</p>

    <h3>Supprimer une photo</h3>
    <p>Cliquez sur <strong>Supprimer</strong> sous la photo concernée.</p>
</div>

<div class="aide-section">
    <h2>Catégories</h2>
    <p>Les catégories organisent les projets dans la navigation du site (Réalisations, Projets en cours, etc.).</p>

    <h3>Créer une catégorie</h3>
    <ol>
        <li>Cliquez sur <strong>Catégories</strong> dans la navigation</li>
        <li>Cliquez sur <strong>Nouvelle catégorie</strong></li>
        <li>Renseignez le nom et cliquez sur <strong>Créer la catégorie</strong></li>
    </ol>

    <h3>Supprimer une catégorie</h3>
    <p>Depuis le formulaire de modification d'une catégorie, cliquez sur <strong>Supprimer</strong>. Une confirmation vous sera demandée. <strong>Attention : la suppression d'une catégorie supprime tous ses projets et photos associés.</strong></p>

    <p>Faites <strong>glisser-déposer</strong> les catégories pour modifier leur ordre dans la sidebar.</p>
</div>

<div class="aide-section">
    <h2>Pages</h2>
    <p>Les pages (Équipe, Contact, etc.) sont éditables depuis la rubrique <strong>Pages</strong>.</p>

    <h3>Créer une page</h3>
    <ol>
        <li>Cliquez sur <strong>Pages</strong> dans la navigation</li>
        <li>Cliquez sur <strong>+ Nouvelle page</strong></li>
        <li>Renseignez le titre et le contenu</li>
        <li>Le <strong>slug</strong> (adresse URL) est généré automatiquement depuis le titre</li>
        <li>Cliquez sur <strong>Créer la page</strong></li>
    </ol>
    <p>La nouvelle page apparaît automatiquement dans la navigation du site public.</p>

    <h3>Modifier une page</h3>
    <p>Depuis la liste des pages, cliquez sur <strong>Modifier</strong> à droite de la page concernée.</p>

    <h3>Supprimer une page</h3>
    <p>Depuis le formulaire de modification, cliquez sur <strong>Supprimer</strong>. Une confirmation vous sera demandée. Cette action est irréversible.</p>
</div>

<div class="aide-section">
    <h2>Éditeur de texte</h2>
    <p>L'éditeur WYSIWYG (descriptions de projets, contenu de pages) propose les outils suivants :</p>
    <ul>
        <li><strong>G</strong> — Gras</li>
        <li><strong>I</strong> — Italique</li>
        <li><strong>Lien</strong> — Insérer un lien hypertexte</li>
        <li><strong>Listes</strong> — Liste numérotée ou à puces</li>
        <li><strong>Gomme</strong> — Supprimer la mise en forme</li>
    </ul>
</div>

<div class="aide-section">
    <h2>Ordre d'affichage</h2>
    <p>L'ordre des éléments sur le site est entièrement contrôlé par le <strong>glisser-déposer</strong> dans l'administration :</p>
    <ul>
        <li><strong>Catégories</strong> — ordre dans la sidebar</li>
        <li><strong>Projets</strong> — ordre au sein de chaque catégorie</li>
        <li><strong>Photos</strong> — ordre d'affichage sur la page projet</li>
    </ul>
    <p>Les modifications d'ordre sont sauvegardées automatiquement.</p>
</div>

<div class="aide-section">
    <h2>Bonnes pratiques</h2>
    <ul>
        <li>Utilisez des images <strong>optimisées pour le web</strong> (JPEG qualité 80-85%, largeur 1600-2400px)</li>
        <li>Renseignez toujours les <strong>crédits photographiques</strong></li>
        <li>Vérifiez l'affichage sur le site après chaque modification (<strong>Voir le site</strong> depuis le tableau de bord)</li>
        <li>Pensez à <strong>vous déconnecter</strong> après utilisation, surtout sur un ordinateur partagé</li>
    </ul>
</div>

</div>
